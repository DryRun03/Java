public class MathOperations
{
    public static void main(String[] args)
    {
        int a = 5;
        int b= 10;
        
        long c= 2500000000L;
        long d = 1000000000000L;
        
        double e= 6.7;
        double f=2.5;
        
        System.out.println(a+b);
        System.out.println(c+d);
        System.out.println(e+f);

        System.out.println(a-b);
        System.out.println(c=d);
        System.out.println(e-f);

        System.out.println(a*b);
        System.out.println(c*b);
        System.out.println(e*f);

        System.out.println(a/b);
        System.out.println(c/d);
        System.out.println(e/f);
        
    }    
}
